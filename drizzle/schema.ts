/**
 * Orders table
 */
export const orders = mysqlTable("orders", {
  id: int("id").autoincrement().primaryKey(),
  userId: int("userId").notNull(),
  totalPrice: decimal("totalPrice", { precision: 10, scale: 2 }).notNull(),
  status: mysqlEnum("status", ["em_analise", "preparando", "entregue"]).default("em_analise").notNull(),
  
  // Delivery information
  street: varchar("street", { length: 255 }).notNull(),
  neighborhood: varchar("neighborhood", { length: 255 }).notNull(),
  number: varchar("number", { length: 50 }).notNull(),
  complement: text("complement"),
  complementType: mysqlEnum("complementType", ["casa", "apartamento", "condominio"]),
  contact: varchar("contact", { length: 20 }).notNull(),
  
  // Payment proof
  proofImageUrl: text("proofImageUrl"),
  
  createdAt: timestamp("createdAt").defaultNow().notNull(),
  updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
});

export type Order = typeof orders.$inferSelect;
export type InsertOrder = typeof orders.$inferInsert;

